const mongoose = require("mongoose");

/* =========================================================
   ====================== LEAD SCHEMA =======================
   ========================================================= */

const leadSchema = new mongoose.Schema(

  {

    /* =====================================================
       ======================== NAME ========================
       ===================================================== */

    name: {
      type: String,
      required: true,
      trim: true
    },

    /* =====================================================
       ======================= PHONE ========================
       ===================================================== */

    phone: {
      type: String,
      required: true,
      trim: true
    },

    /* =====================================================
       ======================== CAR =========================
       ===================================================== */

    carId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Car",
      required: true
    },

    /* =====================================================
       ==================== ASSIGNED TO =====================
       ===================================================== */

    assignedTo: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Admin",
      default: null
    },

    /* =====================================================
       ======================== STATUS ======================
       ===================================================== */

    status: {
      type: String,

      enum: [
        "new",
        "assigned",
        "contacted",
        "interested",
        "negotiation",
        "converted",
        "lost"
      ],

      default: "new"
    },

    /* =====================================================
       ========================= NOTES ======================
       ===================================================== */

    notes: [
      {
        text: {
          type: String,
          trim: true
        },

        createdBy: {
          type: mongoose.Schema.Types.ObjectId,
          ref: "Admin"
        },

        createdAt: {
          type: Date,
          default: Date.now
        }
      }
    ],

    /* =====================================================
       ==================== NEXT FOLLOW-UP ==================
       ===================================================== */

    nextFollowUp: {
      type: Date,
      default: null
    },

    /* =====================================================
       ================= FOLLOW-UP COMPLETED ================
       ===================================================== */

    followUpCompleted: {
      type: Boolean,
      default: false
    },

    /* =====================================================
       ======================== PRIORITY ====================
       ===================================================== */

    priority: {

      type: String,

      enum: [
        "low",
        "medium",
        "high",
        "urgent"
      ],

      default: "medium"
    }

  },

  /* =======================================================
     ======================== OPTIONS =======================
     ======================================================= */

  {
    timestamps: true
  }

);

/* =========================================================
   ========================= EXPORT =========================
   ========================================================= */

module.exports = mongoose.model(
  "Lead",
  leadSchema
);