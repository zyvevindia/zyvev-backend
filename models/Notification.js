const mongoose = require("mongoose");

/* =========================================================
   ================= NOTIFICATION SCHEMA ===================
   ========================================================= */

const notificationSchema = new mongoose.Schema(

  {

    /* =====================================================
       ======================= USER =========================
       ===================================================== */

    user: {

      type: mongoose.Schema.Types.ObjectId,

      ref: "Admin",

      required: true
    },

    /* =====================================================
       ======================= TITLE ========================
       ===================================================== */

    title: {

      type: String,

      required: true,

      trim: true
    },

    /* =====================================================
       ===================== MESSAGE ========================
       ===================================================== */

    message: {

      type: String,

      required: true,

      trim: true
    },

    /* =====================================================
       ======================== TYPE ========================
       ===================================================== */

    type: {

      type: String,

      enum: [

        "lead_assigned",

        "followup_overdue",

        "lead_converted",

        "urgent_lead",

        "system"

      ],

      default: "system"
    },

    /* =====================================================
       ======================= PRIORITY =====================
       ===================================================== */

    priority: {

      type: String,

      enum: [

        "low",

        "medium",

        "high",

        "urgent"

      ],

      default: "medium"
    },

    /* =====================================================
       ======================== READ ========================
       ===================================================== */

    isRead: {

      type: Boolean,

      default: false
    },

    /* =====================================================
       ====================== RELATED LEAD ==================
       ===================================================== */

    lead: {

      type: mongoose.Schema.Types.ObjectId,

      ref: "Lead",

      default: null
    }

  },

  /* =======================================================
     ======================== OPTIONS =======================
     ======================================================= */

  {
    timestamps: true
  }

);

/* =========================================================
   ========================= EXPORT =========================
   ========================================================= */

module.exports = mongoose.model(
  "Notification",
  notificationSchema
);