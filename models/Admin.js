const mongoose = require("mongoose");

/* =========================================================
   ===================== ADMIN SCHEMA =======================
   ========================================================= */

const adminSchema = new mongoose.Schema(

  {

    /* =====================================================
       ======================= NAME =========================
       ===================================================== */

    name: {
      type: String,
      trim: true,
      default: ""
    },

    /* =====================================================
       ======================= EMAIL ========================
       ===================================================== */

    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true
    },

    /* =====================================================
       ===================== PASSWORD =======================
       ===================================================== */

    password: {
      type: String,
      required: true
    },

    /* =====================================================
       ======================== ROLE ========================
       ===================================================== */

    role: {
      type: String,
      enum: ["admin", "sales"],
      default: "sales"
    },

    /* =====================================================
       ======================= STATUS =======================
       ===================================================== */

    isActive: {
      type: Boolean,
      default: true
    },

    /* =====================================================
       ===================== CREATED BY =====================
       ===================================================== */

    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Admin",
      default: null
    }

  },

  /* =======================================================
     ======================= OPTIONS ========================
     ======================================================= */

  {
    timestamps: true
  }

);

/* =========================================================
   ======================= EXPORT ===========================
   ========================================================= */

module.exports = mongoose.model(
  "Admin",
  adminSchema
);